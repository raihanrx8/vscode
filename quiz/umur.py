umur = int(input('masukan umur : '))
if umur <= 5:
    print("umur balita")
if  6 <= umur <= 13:
    print("umur anak anak")
if  14 <= umur <= 25:
    print("umur remaja")
if  26 <= umur <= 35:
    print("umur dewasa")
if  36 <= umur <= 55:
    print("umur orang tua")
if umur >= 56:
    print("umur lansia")